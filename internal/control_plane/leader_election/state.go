package leader_election

import "github.com/sirupsen/logrus"

type CMState int

const (
	Follower CMState = iota
	Candidate
	Leader
	Dead
)

func (s CMState) String() string {
	switch s {
	case Follower:
		return "Follower"
	case Candidate:
		return "Candidate"
	case Leader:
		return "Leader"
	case Dead:
		return "Dead"
	default:
		logrus.Fatal("Invalid leader election state machine state")
	}

	return ""
}
